    {
        Albanil::create([
            'nombre' => $request['nombre'],
            'dui' => $request['dui'],
            'nit' => $request['nit'],
            'direccion' => $request['direccion'],
            'cuenta' => $request['cuenta'],
            'id_cliente' => $request['id_cliente'],
            ]);
        return redirect('albaniles');
    }

